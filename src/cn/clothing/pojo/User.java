package cn.clothing.pojo;

public class User {

	
	private String id;		//用户名
	private String password;		//密码
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
	
}
